#include <stdio.h>

int main() {
        int a = 10;

        a++;
        printf("%d \n",a);

        a++;
        printf("%d \n",a);

        ++a;
        printf("%d \n",a);

        a--;
        printf("%d \n",a);

        a--;
        printf("%d \n",a);

        --a;
        printf("%d \n",a);

        return 0;
}